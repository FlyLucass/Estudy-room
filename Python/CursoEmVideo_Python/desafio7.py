import math;
print("Vamos descobrir o valor do seno conceno e tangente, pelo angulo!")
angulo=float(input("digite o valor do angulo que voce tem: "))
print("\nO seno e: {} \no conceno e: {} \na tangente e: {} ".format(math.sin(math.radians(angulo)),math.cos(math.radians(angulo)),math.tan(math.radians(angulo))))